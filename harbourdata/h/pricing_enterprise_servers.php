              function ajaxprice(table_name, column, my_column, value) {
                  //alert(lj_cpu_price+''+cpu+''+ch+'.'+value);
                  var my_result;
                  $.ajax({
                      url: 'plans/myprice.php',
                      type: "POST",
                      cache: false,
                      async: false,
                      data: 'table_name=' + table_name + '&&column=' + column + '&&my_column=' + my_column + '&&value=' + value,
                      error: function(r){
                     //$("#email_form")[0].reset();
